const report = [];
for (const [cat, rules] of Object.entries(probes)) {
  const products = sections.get(cat) || [];
  report.push(`\n#### ${cat} (${products.length})`);
  const assigned = new Set();
  for (const [sub, patterns] of Object.entries(rules)) {
    let count = 0;
    const samples = [];
    for (const p of products) {
      const n = normalizeAr(p);
      if (patterns.some((re) => re.test(n))) {
        count++;
        if (samples.length < 5) samples.push(p);
        assigned.add(p);
      }
    }
    report.push(`  ${sub}: ${count}  e.g. ${samples.join(' | ')}`);
  }
  report.push(`  UNMATCHED: ${products.length - assigned.size}`);
  const unmatched = products.filter((p) => !assigned.has(p)).slice(0, 30);
  report.push(`  unmatched samples: ${unmatched.join(' || ')}`);
}

fs.writeFileSync(path.join(__dirname, 'category-probe-report.txt'), report.join('\n'), 'utf8');
